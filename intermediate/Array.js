// data structure in js 
//creation 4 methods
// 1. Array Literal
// 2. Array Constructor
// 3. Array.of() method
// 4. Array.from() method

// Array Literal
let arr = [1, 2, 3]; // Array Literal

// Array Constructor
let arr1 = new Array(4, 5, 6); // Array Constructor

// Array.of() method
let arr2 = Array.of(7, 8, 9); // Array.of() method

// Array.from() method
let arr3 = Array.from('hello'); // Array.from() method


console.log(arr); // [1, 2, 3]
console.log(arr1); // [4, 5, 6]
console.log(arr2); // [7, 8, 9]
console.log(arr3); // ['h', 'e', 'l', 'l', 'o']


